# Custom pseudolog10_trans transformation function
pseudolog10_trans_custom <- function() {
  trans_new(
    name = "pseudolog10",
    # Accounts for both positive and negative values by applying the logarithm transformation 
    # after taking the absolute value and then restoring the original sign.
    transform = function(x) {
      sign(x) * log10(abs(x) + 1) * 0.5 # Compresses the transformed values to bring the lines closer to the central axis.
    },
    inverse = function(x) {
      sign(x) * (10^(abs(x) * 2) - 1)
    },
    # Generates "pretty" breaks for the transformed scale.
    breaks = function(x) {
      if (length(x) <= 2) {
        x
      } else {
        pretty(x, n = 6)
      }
    },
    format = "log",
    domain = c(min(cobs3$value), max(cobs3$value))
  )
}

#
find_top_bottom <- function(cl=1, top = TRUE) {
  clust <- cobs3[cobs3$cluster==cl,]
  if(top) {
    # Find the 5 highest entries
    entries <- do.call(rbind, by(clust, clust$Country, function(x) head(x[order(x$value, decreasing = TRUE), ], 1))) %>%
      select(-c(Country, variable)) %>%
      arrange(desc(value)) %>%
      head(5)
  } else {
    # Find the 5 lowest entries
    entries <- do.call(rbind, by(clust, clust$Country, function(x) head(x[order(x$value), ], 1))) %>%
      select(-c(Country, variable)) %>%
      arrange(value) %>%
      head(5)
  }
  return(entries)
}

